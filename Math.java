public class Math {
    public static void main(String[] args) {
        System.out.println("The highest value: " + java.lang.Math.max(2,3)); //Math.max(x,y)
        System.out.println("The lowest value: " + java.lang.Math.min(2,3));  //Math.min(x,y)
        System.out.println("The square root: " + java.lang.Math.sqrt(49));  //Math.sqrt(x)
        System.out.println("The absolute (positive) value: " + java.lang.Math.abs(-1.5)); //Math.abs(x)
        System.out.println("A random number between 0.0 and 1.0: " + java.lang.Math.random()); //Math.random()
    }
}