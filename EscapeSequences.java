public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Look\nhere"); //New line
        System.out.println("Look\rhere"); //Carriage Return
        System.out.println("Look\fhere"); //Form Feed
        System.out.println("Look\there"); //Tab
        System.out.println("Look\bhere"); //Backspace
    }
}
