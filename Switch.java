public class Switch {
    public static void main(String[] args) {
        int color = 10;
        switch(color) {
            case 1:
                System.out.println("red");
                break;
            case 2:
                System.out.println("blue");
                break;
            case 3:
                System.out.println("green");
                break;
            default:
                System.out.println("white");
        }
    }
}
