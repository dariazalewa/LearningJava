public class MultiplicationTable {
    public static void main(String[] args) {
        int n1 = 1, n8 = 8;
        for (int m = 1; m <= 10; m++) {
            System.out.println(n1 + " x " + m + " = " + (n1 * m));
            System.out.println(n8 + " x " + m + " = " + (n8 * m));
        }
    }
}
