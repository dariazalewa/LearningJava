public class NumbersAndStrings {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;
        int c = a + b;
        System.out.println(c);

        String d = "10";
        String e = "10";
        String f = d + e;
        System.out.println(f);

        int g = 10;
        String h = "10";
        String i = g + h;
        System.out.println(i);
    }
}
